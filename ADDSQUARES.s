COUNT		RN 1
NUMBER		RN 2
ENTRY	MOV SUM, #1
		MOV COUNT, #1
		MOV NUMBER, #1
LOOP	ADD NUMBER, NUMBER, #1
		BL SQU
		ADD SUM, SUM, R3
		ADD COUNT, COUNT, #1
		CMP COUNT, #5
		BNE LOOP
STOP 	B STOP
SQU		MUL R3, NUMBER, NUMBER
		BX LR
		END
		