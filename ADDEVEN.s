		MOV COUNT, #5
		MOV NUMBER, #0
		LDR R3, =0X40000000;
		SUBS COUNT, COUNT, #1
		BEQ STOP
LOOP	ADD NUMBER, NUMBER, #2
		ADD SUM, SUM, NUMBER
		SUBS COUNT, COUNT, #1
		BNE LOOP
		STR SUM, [R3]
STOP	B STOP
		END
