			AREA COMB, CODE, READONLY
DIVIDEND	RN 4
DIVISOR		RN 5
QUOTIENT	RN 3
REMAINDER	RN 2
N			RN 1
R			RN 0
NDR			RN 6
ENTRY
		MOV N, #5
		MOV R, #3
		SUB NDR, N, R
		MOV DIVIDEND, N
		BL FACT
		MOV N, DIVISOR
		MOV DIVIDEND, R
		BL FACT
		MOV R, DIVISOR
		MOV DIVIDEND, NDR
		BL FACT
		MUL DIVISOR, R, DIVISOR
		MOV DIVIDEND, N
		BL LOOP
STOP	B STOP
FACT	MOV DIVISOR, #1
FACT1	MUL DIVISOR, DIVIDEND, DIVISOR
		SUBS DIVIDEND, DIVIDEND, #1
		BNE FACT1
		BX LR
LOOP	MOV QUOTIENT, #0
DIV		SUBS DIVIDEND, DIVIDEND, DIVISOR
		ADDPL QUOTIENT, QUOTIENT, #1
		BPL DIV
		ADDMI REMAINDER, DIVIDEND, DIVISOR
		BX LR
		END
